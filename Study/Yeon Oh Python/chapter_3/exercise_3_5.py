def average_of_4_numbers(a, b, c, d):
    """네 개의 수를 입력받아 평균을 계산한다."""
    print((a + b + c + d) / 4)

print('첫번째 수를 입력하세요.')
num_1 = int(input())

print('두번째 수를 입력하세요.')
num_2 = int(input())

print('세번째 수를 입력하세요.')
num_3 = int(input())

print('네번째 수를 입력하세요.')
num_4 = int(input())

average_of_4_numbers(num_1, num_2, num_3, num_4)